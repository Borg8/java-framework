package borg.framework.structures;

import org.eclipse.jdt.annotation.NonNull;

public class TypedEntity<T extends Enum<T>>
{
	//////////////////////////////////////////////////////////////////////////////////////////////////
	// Constants
	//////////////////////////////////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// Definitions
	//////////////////////////////////////////////////////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// Fields
	//////////////////////////////////////////////////////////////////////////////////////////////////

	/** entity type **/
	@NonNull
	public final T type;

	//////////////////////////////////////////////////////////////////////////////////////////////////
	// Methods
	//////////////////////////////////////////////////////////////////////////////////////////////////

	public TypedEntity(@NonNull T type_)
	{
		type = type_;
	}

	@Override
	public boolean equals(Object object_)
	{
		if (object_ instanceof TypedEntity)
		{
			return type == ((TypedEntity<?>)object_).type;

		}

		return false;
	}

	@Override
	public int hashCode()
	{
		return type.ordinal();
	}

	@Override
	public String toString()
	{
		return "type: " + type;
	}
}
